package Day1;

public class Primitive_Data_type {

	public static void main(String[] args) {
		// there is 8 primitive data types available in java 
		
		byte a1=Byte.MAX_VALUE;
		byte a=Byte.MIN_VALUE;
		
		short b1=Short.MAX_VALUE;
		short b=Short.MIN_VALUE;
		
		int c1=Integer.MAX_VALUE;
		int c=Integer.MIN_VALUE;
		
		long d1=Character.MAX_VALUE;
		long d=Character.MIN_VALUE;
		
		float e1=Float.MAX_VALUE;
		float e=Float.MIN_VALUE;
		
		long f1=Long.MAX_VALUE;
		long f=Long.MIN_VALUE;
		
		double g1=Double.MAX_VALUE;
		double g=Double.MIN_VALUE;
		
		boolean h1=true;
		boolean h=false;
		
		System.out.println("byte min :"+a);

		System.out.println("byte max :"+a1);

		System.out.println("short min :"+b);
		System.out.println("short max :"+b1);

		System.out.println("int min :"+c);

		System.out.println("int max :"+c1);

		System.out.println("char min :"+d);

		System.out.println("char max :"+d1);

		System.out.println("float min :"+e);

		System.out.println("float max :"+e1);

		System.out.println("long min :"+f);

		System.out.println("long max :"+f1);

		System.out.println("double min :"+g);

		System.out.println("double max :"+g1);

		System.out.println("boolean :"+h);

		System.out.println("boolean :"+h1);
	}

}
